const assetsByAction = humiCharacterAssets.reduce((groups, asset) => {
  if (!groups[asset.action]) groups[asset.action] = [];
  groups[asset.action].push(asset);
  return groups;
}, {});

export function getHumiCharacterCandidates(variant = "kitchen") {
  const actions = humiCharacterVariantPools[variant] ?? humiCharacterVariantPools.kitchen;
  return actions.flatMap((action) => assetsByAction[action] ?? []);
}

export function pickHumiCharacterIllustration(variant = "kitchen", options = {}) {
  const {
    seed = "humi",
    contextKey = variant,
    usedIds = [],
    usedActions = [],
    preferGender,
  } = options;
  const candidates = getHumiCharacterCandidates(variant);

  if (candidates.length === 0) {
    return {
      id: `legacy-${variant}`,
      action: variant,
      gender: "u",
      src: legacyIllustrationMap[variant] ?? legacyIllustrationMap.kitchen,
      legacy: true,
    };
  }

  const usedIdSet = new Set(usedIds);
  const usedActionSet = new Set(usedActions);
  const knownGenderCandidates = candidates.filter((asset) => asset.gender === "m" || asset.gender === "f");
  const genderPool = knownGenderCandidates.length > 0 ? knownGenderCandidates : candidates;
  const targetGender = preferGender ?? (stableHash(`${seed}:${contextKey}:gender`) % 2 === 0 ? "f" : "m");

  const ranked = [...genderPool].sort((a, b) => {
    const aScore = scoreAsset(a, { targetGender, usedIdSet, usedActionSet, seed, contextKey });
    const bScore = scoreAsset(b, { targetGender, usedIdSet, usedActionSet, seed, contextKey });
    return aScore - bScore;
  });

  return ranked[0] ?? candidates[stableHash(`${seed}:${contextKey}:fallback`) % candidates.length];
}

function scoreAsset(asset, { targetGender, usedIdSet, usedActionSet, seed, contextKey }) {
  let score = 0;
  if (usedIdSet.has(asset.id)) score += 10_000;
  if (usedActionSet.has(asset.action)) score += 1_000;
  if (asset.gender === "u") score += 200;
  if (asset.gender !== "u" && asset.gender !== targetGender) score += 40;
  score += stableHash(`${seed}:${contextKey}:${asset.id}`) % 37;
  return score;
}

export function stableHash(input) {
  const text = String(input);
  let hash = 2166136261;
  for (let index = 0; index < text.length; index += 1) {
    hash ^= text.charCodeAt(index);
    hash = Math.imul(hash, 16777619);
  }
  return hash >>> 0;
}
